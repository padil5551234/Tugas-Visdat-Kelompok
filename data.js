// Fungsi interpretasi sinyal per provinsi
function getSinyalInterpretasi(provName) {
  const d = sinyalData.find(s => s.provinsi === provName);
  if (!d) return 'Data tidak tersedia untuk provinsi ini.';
  const baikPct = d.kuat + d.sangat_kuat;
  const burukPct = d.sangat_lemah + d.lemah;
  if (baikPct >= 90) return `${provName} termasuk provinsi dengan infrastruktur telekomunikasi terbaik. Hampir semua desa sudah terjangkau sinyal kuat hingga sangat kuat (${baikPct.toFixed(1)}%). Konektivitas seluler sudah merata dan mendukung aktivitas digital sehari-hari.`;
  if (baikPct >= 75) return `${provName} memiliki penerimaan sinyal yang baik secara keseluruhan. Sebagian besar desa (${baikPct.toFixed(1)}%) sudah dijangkau sinyal kuat, meski masih ada daerah terpencil yang perlu perhatian.`;
  if (baikPct >= 55) return `${provName} berada di kategori menengah. Sinyal kuat mencakup ${baikPct.toFixed(1)}% desa, namun ${burukPct.toFixed(1)}% masih mengalami sinyal lemah. Perlu perluasan infrastruktur BTS di wilayah pedalaman.`;
  if (baikPct >= 40) return `${provName} masih menghadapi tantangan konektivitas yang serius. Hanya ${baikPct.toFixed(1)}% desa yang mendapat sinyal memadai. Investasi mendesak diperlukan untuk menjembatani kesenjangan digital.`;
  return `${provName} merupakan wilayah dengan kondisi sinyal paling kritis. Hanya ${baikPct.toFixed(1)}% desa yang terjangkau sinyal kuat/sangat kuat, sementara ${burukPct.toFixed(1)}% desa masih bergulat dengan sinyal sangat lemah. Diperlukan intervensi kebijakan dan infrastruktur yang masif.`;
}

// Fungsi interpretasi penetrasi internet per provinsi
function getPenetrasiInterpretasi(provName) {
  const d = provinsiData.find(p => p.provinsi === provName);
  if (!d) return 'Data tidak tersedia untuk provinsi ini.';
  const v = d.y2024;
  const trend = d.y2023 != null ? (v - d.y2023).toFixed(2) : null;
  const trendStr = trend !== null ? ` (naik ${trend}pp dari 2023)` : '';
  if (v >= 87) return `${provName} merupakan provinsi dengan penetrasi internet tertinggi di Indonesia (${v}%${trendStr}). Tingginya konektivitas didukung oleh infrastruktur digital yang matang dan kepadatan penduduk perkotaan.`;
  if (v >= 80) return `${provName} termasuk dalam kelompok provinsi dengan penetrasi internet sangat tinggi (${v}%${trendStr}). Sebagian besar penduduk sudah menikmati akses internet dan memanfaatkannya untuk berbagai kegiatan.`;
  if (v >= 70) return `${provName} berada pada penetrasi internet yang baik (${v}%${trendStr}). Pertumbuhan pengguna internet terus meningkat, namun masih ada ruang untuk memperluas jangkauan ke daerah terpencil.`;
  if (v >= 60) return `${provName} mencatat penetrasi internet menengah (${v}%${trendStr}). Lebih dari separuh penduduk telah terkoneksi, namun masih banyak yang belum mendapat akses memadai.`;
  if (v >= 45) return `${provName} masih berada di bawah rata-rata nasional (${v}%${trendStr}). Diperlukan dorongan infrastruktur dan literasi digital untuk mempercepat inklusi digital.`;
  return `${provName} termasuk provinsi dengan penetrasi internet terendah (${v}%${trendStr}). Tantangan geografis dan keterbatasan infrastruktur menjadi hambatan utama akses internet bagi penduduk.`;
}

